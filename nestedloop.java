public class nestedloop { //This line defines the start of a Java class named "nestedloop."
    public static void main(String[] args) {
        for(int i=1;i<=10;i++){// If i is equal to 5, the break statement is executed, which immediately exits the loop.
            if(i==5){
                break;
            }
            System.out.println(i);// the message to be printed or shown in output.
        }
    }
}

